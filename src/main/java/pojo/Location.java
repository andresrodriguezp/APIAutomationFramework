package pojo;

public class Location {
	
	//here we are creating a sub pojo class
	
	private double lat; //here we are setting double because the value expected is a negative and with decimals
	private double lng; //here we are setting double because the value expected is a negative and with decimals
	
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	



}
